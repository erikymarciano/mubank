package aplicacao;

/**
 *
 * @author Matth
 */
public class Conta {
    private int id;
    private double saldo;
    private int id_usuario;

    public Conta() {
    }

    public Conta(int id, double saldo, int id_usuario) {
        this.id = id;
        this.saldo = saldo;
        this.id_usuario = id_usuario;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }

    public int getIdUsuario() {
        return id_usuario;
    }

    public void setIdUsuario(int id_usuario) {
        this.id_usuario = id_usuario;
    }


}